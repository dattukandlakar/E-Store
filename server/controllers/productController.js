const Product = require('../models/Product');
const Category = require('../models/Category');

// @desc    Fetch all products
// @route   GET /api/products
// @access  Public
const getProducts = async (req, res) => {
  try {
    const pageSize = 12;
    const page = Number(req.query.pageNumber) || 1;

    const keyword = req.query.keyword
      ? {
          name: {
            $regex: req.query.keyword,
            $options: 'i',
          },
        }
      : {};


    const minPrice = req.query.minPrice ? { price: { $gte: Number(req.query.minPrice) } } : {};
    const maxPrice = req.query.maxPrice ? { price: { $lte: Number(req.query.maxPrice) } } : {};

    // Category filtering via query: category (name) or categoryId
    let categoryFilter = {};
    if (req.query.categoryId) {
      categoryFilter = { category: req.query.categoryId };
    } else if (req.query.category) {
      const categoryName = req.query.category;
      const categoryDoc = await Category.findOne({ name: { $regex: `^${categoryName}$`, $options: 'i' } });
      if (!categoryDoc) {
        return res.json({ products: [], page, pages: 0, total: 0 });
      }
      categoryFilter = { category: categoryDoc._id };
    }

    const count = await Product.countDocuments({ ...keyword, ...minPrice, ...maxPrice, ...categoryFilter });
    const products = await Product.find({ ...keyword, ...minPrice, ...maxPrice, ...categoryFilter })
      .limit(pageSize)
      .skip(pageSize * (page - 1))
      .sort({ createdAt: -1 });

    res.json({
      products,
      page,
      pages: Math.ceil(count / pageSize),
      total: count,
    });
  } catch (error) {
    console.error(error);
    res.status(500).json({ message: 'Server error' });
  }
};

// @desc    Fetch single product
// @route   GET /api/products/:id
// @access  Public
const getProductById = async (req, res) => {
  try {
    const product = await Product.findById(req.params.id);

    if (product) {
      res.json(product);
    } else {
      res.status(404).json({ message: 'Product not found' });
    }
  } catch (error) {
    console.error(error);
    res.status(500).json({ message: 'Server error' });
  }
};

// @desc    Delete a product
// @route   DELETE /api/products/:id
// @access  Private/Admin
const deleteProduct = async (req, res) => {
  try {
    const product = await Product.findById(req.params.id);

    if (product) {
      await product.remove();
      res.json({ message: 'Product removed' });
    } else {
      res.status(404).json({ message: 'Product not found' });
    }
  } catch (error) {
    console.error(error);
    res.status(500).json({ message: 'Server error' });
  }
};

// @desc    Create a product
// @route   POST /api/products
// @access  Private/Admin
const createProduct = async (req, res) => {
  try {
    const {
      name,
      description,
      price,
      imageUrl,
      stock,
      category,
      brand,
      featured,
      tags,
    } = req.body;

    const product = new Product({
      name,
      description,
      price,
      imageUrl,
      stock,
      category,
      brand,
      featured: featured || false,
      tags: tags || [],
    });

    const createdProduct = await product.save();

    res.status(201).json(createdProduct);
  } catch (error) {
    console.error(error);
    res.status(500).json({ message: 'Server error' });
  }
};

